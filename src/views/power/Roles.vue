.el-tag {
  margin: 7px;
}
.bdtop {
  border-top: 1px solid #eee;
}
.bdbottom {
  border-bottom: 1px solid #eee;
}
/* 设置权限居中 */
.vcenter {
  display: flex;
  align-items: center;
}
